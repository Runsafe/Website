<h1>What is Huckleberry?</h1>
<p>Huckleberry is a custom-blend Minecraft client developed by Runsafe to offer a new game-play experience inside Minecraft focused on adventure and RPG aspects. Our aim is to bring more items to find, more exciting places to explore and a wide range of different things to create a more immersion-driven adventure for you. Huckleberry is fully online with a dedicated server maintained by Runsafe alongside our other online services.</p>
